const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let result = [];

    for (let i = 0; expr.length > 0; i++) {
        let newStr = expr.slice(0,10);
        expr = expr.slice(10, expr.length);

        if (newStr === '**********') {
        result.push(' ');
        }
        else {
        let base = newStr.replace(/10/g, '.').replace(/11/g, '-').replace(/00/g,'');
        result.push(MORSE_TABLE[base]);
        }


    }

    return result.join('');
}

module.exports = {
    decode
}